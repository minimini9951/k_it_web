package j03_연산자;

public class Operation3 {

	public static void main(String[] args) {
		
		// 복합대입연산자
		int num = 10;
		System.out.println(num);
		
		num = num + 10;
		System.out.println(num);
		
		num += 10;
		System.out.println(num);
		
		num /= 2;
		System.out.println(num);
		
		num %= 4;
		System.out.println(num);
	}

}
