package j07_반복;

public class For1 {

	public static void main(String[] args) {
		
		System.out.println("시작");
		for(int i = 0; i < 10; i++) {
			System.out.println("i = " + i);
		}
		System.out.println("종료");
		
		/*
		 * for(초기치; 조건문; 증가치) {
		 * 				<실행할 문장A>
		 * 				<실행할 문장B>
		 * ...
		 * }
		 */
	}

}
